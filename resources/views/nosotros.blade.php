                    <h2 class="mb-3" data-aos="fade-up">Nosotros</h2>
                </div>

                <div class="col-lg-9 col-12 mt-3 mb-lg-5">
                    <p class="me-4" data-aos="fade-up" data-aos-delay="300">
                        <strong>NUESTRA HISTORIA</strong><br>

                        <span><u>Los Inicios:</u></span><br>
                        Nuestra cooperativa se formo ya hace más de quince años. Éramos un grupo de trabajadores de la
                        industria de la confección que en el medio de la crisis económica del país en esa época, más
                        agravada en Paraná por los Federales, enfrentábamos una situación muy complicada porque no había
                        forma de conseguir trabajo formal y fijo, nos manteníamos haciendo trabajos a fasón en nuestras
                        casas, talleres informales chiquitos, grupos de tres o cuatro; confeccionando para escuelas,
                        clubes, entre otros; utilizando maquinas ovlerlocks y rectas, de cada uno, algunas familiares y
                        otras semi industriales; pero todos estos trabajos eran inconstantes. La mayoría nos conocíamos
                        por haber trabajado en la fábrica Oro-tex de Oro Verde, que cerró en 1999 y estaba dedicada a
                        fabricar principalmente jeans a fasón para marcas de Córdoba y líneas de ropa informal de las
                        marcas reconocidas comos Efesis, Exes, Tango, Lobo, Onza, etc. Luego del cierre quedó un parque
                        de máquinas viejas que pudimos mantener y con las cuales pudimos trabajar desde nuestras
                        casas.<br>
                        Ya a inicios de 2004 vimos la posibilidad de conformar la cooperativa formalmente, por estatuto.
                        Esto nos brindaría tres cosas:                    
                        <p>1- Nos permitiría empezar a comprometernos como grupo, brindarnos ayuda mutua, darle una
                            forma o
                            marco y poder lograr la estabilidad con la que hasta ese momento no contábamos; tratábamos
                            de
                            juntarnos para conseguir trabajos mas grandes o ayudas del gobierno, pero si a alguien le
                            salía
                            algo particular se iba, se sumaban otros que después se iban también.
                        </p>
                        <p>2- Poder empezar a confeccionar y vender nuestros productos tanto a la municipalidad como a
                            la
                            provincia, a distintos entes y clientes que valoraban la calidad de nuestro trabajo, pero
                            que no
                            podían comprarnos si no era con facturas y como sociedad bien formal, con cuenta de banco y
                            papeles.
                        </p>
                        <p>3- Al formar la cooperativa de trabajo podíamos inscribirnos en monotributos social, lo que
                            nos
                            cubriría la parte de obra social y jubilación, empezar a encontrar una formalidad,
                            eventualmente
                            tarjeta y la posibilidad de obtener un crédito.
                        </p>                    
                    </p>
                    <p style = “margin-left: 40px” class="me-4" data-aos="fade-up" data-aos-delay="300">
                        <span><u>Estatuto y matrícula:</u></span><br>
                        Entonces a fines del año 2003 , en la Dirección de Cooperativas de cita en calle Gualeguaychu
                        322,hicimos las capacitaciónes correspondiente y elaboramos el estatuto. Nos asesoro en ese momento
                        un contador,con el estudio en Barrio Rocamora sobre calle Provincias Unidas, que se asesoraba a las
                        cooperativas de los planes trabajar.<br>
                          Pero los trámites eran lentos, había que mandar todo al INAC a Buenos Aires, fue y volvió con
                        unos errores o correcciones hasta que
                        finalmente con fecha 17/06/2004 se nos otorga la Matricula N° 26629 a la "Cooperativa de Trabajo
                        del Centro Ltda."<br>
                        -Hoy nadie se acuerda a quien se le ocurrió o porque el nombre "del Centro"-, dedicada a la
                        confección de uniformes y ropa de trabajo y elaboración
                        de alimentos y harina -nuevamente, nadie sabe o se acuerda porque pusieron esa actividad
                        alimenticia, no tenía nada que ver, fue un error de tipeo,
                        pero después nos generaría dolores de cabeza en todas las inscripciones….
                    </p>

                    <p class="me-4" data-aos="fade-up" data-aos-delay="300">
                        <span><u>Primer galpón:</u></span><br>
                        Un hito en nuestra historia fue en marzo de dos mil seis , cuando pudimos alquilar formalmente e
                        instalarnos de modo definitivo en la que es nuestra
                        casa desde entonces, calle Emilio Caraffa 2545, entre Alvarez Condarco y Ruben Alday, en Barrio
                        Belgrano.<br>
                        En ese momento era un galpón de 30 x 20, con uso anterior de taller de camiones, con piso de
                        cemento mejorado y techos de chapa. Se le hicieron un sinfín de modificaciones, tuvimos que
                        rehacer la carpeta y cambiar casi la mitad de las chapas, pintarlo, colocar extractores de aire,
                        revocarlo, realizar las instalaciones eléctricas, entre otras; y si bien no era para nada
                        cómodo, significaba para nosotros tener nuestro establecimiento propio, un lugar de trabajo
                        distinto de los garages y tallercitos de nuestras casas, de lugares prestados, un lugar donde
                        poder realizar producción en bloque, empezar y terminar el producto, y poder mostrarnos y
                        recibir a nuestros clientes y proveedores como algo real, tangible, confiable.<br>
                        Desde ese momento no se ha parado de crecer, lento pero seguro, tratando de conseguir mas
                        maquinas, comprando o canjeando, mejorando las instalaciones, la presentación de las prendas.
                        Con mucho esfuerzo y sacrificio alquilamos el galpón de al lado en el año dos mil ocho y luego,
                        en dos mil once otro más, contando actualmente con tres galpones, en total mil seiscientos
                        metros, todos alquilados al mismo propietario que, mas allá de que por supuesto se le abona al
                        alquiler correspondiente, nos ha acompañado siempre, comprensivo en los momentos de crisis,
                        siempre teniendo en cuenta las posibilidades reales de una cooperativa como la nuestra,
                        alentándonos a seguir creciendo.
                    </p>
                    <p class="me-4" data-aos="fade-up" data-aos-delay="300">
                        <span><u>Dolores en el crecimiento:</u></span><br>
                        En este camino de crecimiento se fueron incorporando nuevos asociados, a los cuales siempre se
                        les brindan capacitaciones para poder realizar los trabajos. Algo muy importante para destacar
                        de nuestra cooperativa es la posibilidad que se les brinda a los asociados de aprender y
                        entrenarse, dándoles un oficio. Se puede decir que la cooperativa funciona como un “taller
                        escuela”, ya que al no estar dentro de un polo industrial textil no se consigue mano de obra
                        calificada, sino que se va formando y capacitando desde cero, sin necesidad de un conocimiento
                        básico previo.<br>
                        La mayoría de los nuevos asociados es gente joven que ha crecido en otro contexto, "segunda
                        generación", sin el derrotero en común de historia laboral que si tenía el grupo original. Como
                        pasa en todo grupo humano, estas diferencias generan, a veces algunas tensiones o discusiones,
                        cuantos mas somos a veces es mas dificil ponerse de acuerdo, y en ese camino la soulcion fue
                        siempre volver a las fuentes, a los principios y valores del Cooperativismo.<br>
                        Todos temas y planteos que anteriormente no habían surgido pues nos conocíamos entre nosotros,
                        era un grupo mas homogéneo, se sentía en el aire la sensación del esfuerzo en común, capaz que
                        hasta nostalgia, de lo que había costado construir todo esto, las jornadas interminables en
                        tiempos en que hablar de aire acondicionado era impensable, en que si había que terminar un
                        trabajo para poder cobrarlo y repartir no había feriado ni fin de semana largo que valga.
                        Ese estado de de deliberaciones nos llevo a acordar y poner por escrito un Reglamento Interno,
                        que costó muchísimo definir y para el cual, después, tuvimos que investigar mucho en internet
                        porque nos enteramos que tenía que ser aprobado y visado por INAES y no debía contradecir el
                        estatuto ni la Ley de Cooperativas y sus múltiples interpretaciones. Pero era necesario para
                        ordenar mejor las relaciones internas entre asociados de modo que se pueda cumplir el objeto de
                        trabajo de la cooperativa, lo cual sino se estaba volviendo imposible; no había orden y algunos
                        de los que recién ingresaban no sentían ni conocían el espíritu cooperativo ni el
                        funcionamiento.<br>
                        Claro que en la mayoría de los casos, estas proclamas llegaban hasta ahí nomas, porque al
                        momento en que se invitaba a los "proclamadores" a asumir responsabilidades directivas del dia a
                        dia, en las reuniones de planificación del trabajo con propuestas firmes sobre problemas reales
                        del "conjunto de los asociados", y del dia a dia que son muchísimos (como traer nuevos clientes,
                        atender sus reclamos, asegurar las cobranzas para poder cubrir la cuenta y los cheques, y el
                        reparto de anticipos de excedentes y retornos, atender a los proveedores que reclaman, pagar los
                        impuestos, negociar con el banco, ternos e internos) , ahí algunos "se les pasaba el ímpetu" y
                        el interés y la energía por participar activamente en la dirección de la cooperativa.
                        Este es un problema que todavía tenemos, pero que a partir del tener vigente el Reglamento
                        Interno hemos podido, un poco, encauzar.<br>
                        Aun así, por supuesto hay, dentro de un universo de cien asociados, quienes no muestran
                        fortisimo interés por el conjunto ni el devenir de la cooperativa, que de alguna forma, se
                        asocian al principio con el objetivo individual, valido al fin, de trabajar y obtener un ingreso
                        para vivir, sin entrar en preocupase, aun, y ocuparse respecto del funcionamiento de lo global
                        del futuro de una cooperativa. Es una realidad de los tiempos de hoy, y si bien no lo promovemos
                        y si todos tuviésemos esa actitud individualista todo se caería, también creemos que hay ser
                        comprensivos de la situación de cada uno, quizás quien hoy tenga esa visión individualista o
                        pasajera, dentro de dos o cinco años, en otra etapa de su vida personal, tenga la energía o la
                        visión o la motivación para poder poner parte de su esfuerzo en lo colectivo y cooperativo.
                    </p>
                    <p class="me-4" data-aos="fade-up" data-aos-delay="300">
                        <span><u>Más formalidad -> más progreso:</u></span><br>
                        Volviendo un poco para atrás, la primer Habilitación Municipal de la Fabrica la tuvimos hace ya
                        mas de una decada, en diciembre de dos mil seis y a duras penas, porque para lograrla hubo que
                        afrontar con recursos propios, Estudios Ambientales, de Ruido, de Seguridad e Higiene y un
                        oneroso sistema Antiincendio compuesto por un un tanque de reserva de agua de 10.000 litros,
                        tres bombas, cañería especifica, mangueras de emergencia, rehacer casi toda la instalación
                        eléctrica, sensores de alarma de humo.<br>
                        Como siempre en su momento costo mucho decidirse porque "es un montón de plata, y parecía mejor
                        repartirla o comprar máquinas para producir mejor".<br>
                        Pero al final, claro que vale la pena, porque a la larga, es nuestra segunda casa, y aunque
                        parezca frase armada, lo más importante es la salud y la seguridad de todos.
                        En parte por suerte, gracias a Dios, y también por ser prevenidos y cuidadosos, en los años que
                        venimos trabajando ininterrumpidamente en este lugar no hemos tenido que lamentar ningún
                        accidente ni cerca de grave, mas allá de algún pinchazo de aguja en los dedos. Tocamos madera.
                        Pero esa Habilitación Municipal, nos abrió también nuevas puertas:<br>
                        – No teníamos que estar más sin llamar la atención, para que no nos caiga una inspección
                        municipal y nos clausure por falta de habilitación, un miedo que siempre teníamos latente.<br>
                        – Pudimos contratar un seguro colectivo de accidente personales, que nos cubre en esos casos y
                        que antes las aseguradoras se negaban, por no tener precisamente esa habilitación.<br>
                        – A partir ahí pudimos inscribirnos como Proveedor del Estado en Municipios y Provincias, en las
                        cuales la habilitación municipal es, en general, un requisito excluyente. Nos permitió empezar a
                        participar de Licitaciones Públicas, que nos lleguen invitaciones a Licitaciones Privadas y
                        Concursos de Precios, Etc.<br>
                        Se obtuvo también la Inscripción al Registro Industrial de la Nación, ente dependiente del
                        Ministerio de Produccion de la Nacion.
                    </p>
                    <p class="me-4" data-aos="fade-up" data-aos-delay="300">
                        <span><u>INTI -> Certificación de compromiso social:</u></span><br>
                        A partir del año 2017 hemos venido trabajando en conjunto con el INTI, en el programa de
                        Certificación de Compromiso Social Compartido (INTI – CSC).
                        El Centro INTI-Textiles y el Programa de Certificación INTI han decidido lanzar esta
                        iniciativa, aportando sus recursos humanos y tecnológicos para la consolidación de un modelo de
                        producción y consumo sustentable. A esta iniciativa se llega incorporando a los ya tradicionales
                        conceptos de gestión de la calidad, los nuevos modelos de responsabilidad social, ya que no
                        podría concebirse la “calidad” de un producto fabricado bajo condiciones degradantes de la
                        condición humana o el ambiente. <br>
                        Las empresas que adhieran al programa, implicando en el mismo a
                        toda su red de proveedores, una vez que cumplan los requisitos del mismo, y sean auditadas por
                        el INTI, recibirán del Instituto una Certificación que acredita su compromiso «Compromiso Social
                        Compartido” significa que, si bien es la empresa el sujeto central de esta transformación, el
                        INTI asume y comparte desde su rol específico dicho esfuerzo, y mantiene una convocatoria
                        abierta a los consumidores y otros actores civiles y gubernamentales para acompañar este
                        desafío. Esta iniciativa se encuadra dentro de un escenario global y local que ha resultado en
                        una fuerte precarización de las condiciones de trabajo para la gran mayoría de las personas que
                        se desempeñan en las industrias intensivas en mano de obra, entre ellas y particularmente, la de
                        indumentaria.<br>
                         La tragedia vivida en un taller de la ciudad de Buenos Aires hace pocos meses,
                        constituyó un disparador para la toma de conciencia y acción de todos los actores involucrados.
                        La definición de una serie de principios y requisitos de Compromiso Social Compartido para
                        Empresas de Indumentaria, tiene como núcleo principal contribuir progresivamente, a la mejora de
                        las condiciones de trabajo dentro de este sector. A la vez, se entiende que esta propuesta
                        resulta no solo compatible sino pro-activa con la búsqueda de una competitividad a largo plazo,
                        y podría ser incorporada por todas aquellas organizaciones interesadas en formar parte de un
                        sector de indumentaria con futuro".<br>
                        Destacamos que ya hemos pasado tres evaluaciones semestrales, y en cada caso hemos recibido
                        honestas felicitaciones por la calidad del ambiente en que trabajamos, especialmente en
                        comparación con talleres de las provincias y conurbanos mas grandes del pais.
                    </p>                    

                    <p class="me-4" data-aos="fade-up" data-aos-delay="300">
                        <span><u>La situación actual:</u></span><br>
                        Es por todos conocidos que la situación económica general, que impacta especialmente en la
                        actividad textil, con cierres y suspensiones en fabricas de todo el país, está en todos los
                        diarios, en las estadísticas oficiales y no oficiales, y se puede ver en la calle, por lo que no
                        vale la pena ahondar en explicarlo, solo se puede esperar o rezar que no se agrave y que cuanto
                        antes se pueda empezar a revertir.<br>
                        Caída abrupta de demanda, aumento de los servicios, especialmente la Electricidad que para
                        nosotros es totalmente esencial, del alquiler, aumento de insumos y repuestos "dolarizados",
                        caída de cadenas de pagos con aumento de cheques rechazados, imposibilidad de acceder a créditos
                        y, en caso de acceder, es a tasas de descubierto que queman todo.<br>
                        La situación de nuestra Cooperativa es difícil pero más difícil es la de cada una de nuestras
                        familias, los ingresos como anticipo de excedentes o retorno alcanzan cada vez menos para cubrir
                        el costo de vida, que se dispara, especialmente la canasta de alimentos e insumos básicos, por
                        arriba incluso de la inflacion.<br>
                        Cada uno de nosotros hemos tenido que realizar ajustes en nuestra casa, pero hay un estres muy
                        grande por si aumentar y cuanto los anticipos de excedentes, dejando de pagar o atrasando otras
                        cuentas como servicios, alquiler, impuestos, proveedores que también son esenciales para el
                        funcionamiento futuro, porque si no se pagan esas cuentas a la larga no podemos seguir
                        trabajando. Es muy injusto, porque esto no ocurre por nuestra culpa, sino que son factores
                        externos que impactan a todas las fábricas textiles, y no textiles, del país. Es una situación
                        muy disgregante de espíritu colectivo y cooperativo, cuando las necesidades mínimas de las
                        familias están cada vez más insatisfechas, porque todo está más caro, porque alguien de la
                        familia se quedó sin trabajo, etc.<br>
                        En nuestra ya no tan corta historia, siempre hemos intentado y logrado subsisitir y crecer sin
                        depender de regimenes de ayudas o subsidios de parte del gobierno ni nacional, ni provincial, ni
                        municipal, ni de ninguna agrupacion politica. Hemos con gran esfuerzo llegado hasta aca, siendo
                        autostentables y de lo cual estamos muy orgullosos, cualquiera sea el destino final que nos
                        toque.<br>
                        Hoy, que cierran sus puertas hasta empresas multinacionales, decimos con la frente alta que
                        ahora si, sin ninguna dudad, necesitamos ciertamente de esa ayuda o colaboracion, apoyo de parte
                        del gobierno y de los politicos, sin entrar a hacer diferencias en cuanto a signo partidario o
                        nivel estamental.<br>
                        Nos une la necesidad de trabajar y ganar nuestro pan siendo dueños de nuestro futuro, que al
                        hacerlo en grupo y de manera solidaria, se transforma en Impulso Cooperativo.
                    </p>

                    <p class="me-4" data-aos="fade-up" data-aos-delay="300">
                        <span><u>Autoridades:</u></span><br>
                        La primer Presidencia estuvo a cargo de Ines Portizio, en la previa y en el primer año de conformación. Luego
                        precedió dicho cargo Norberto Arce, desde el año dos mil cinco hasta dos mil ocho
                        que se retiro de la cooperativa por haber recibido una oportunidad mejor de trabajo, pero siguio
                        acompañándonos y apoyándonos en lo que está a su alcance, desde su nuevo lugar. Lo siguió en la
                        Presidencia Gustavo Romero, que había ingresado a la cooperativa en el área de planchado y
                        embalaje, y que actualmente y desde hace unos cinco años dirige el área de corte y molderia.
                        Estuvo en dicho cargo durante nada menos que diez años de crecimiento institucional.
                        Desde mayo de dos mi diecisiete , el presidente fue Silvio Pucheta, que se asocio a nuestra
                        cooperativa recién en el año dos mil siete , proveniente de la Ciudad de Córdoba, donde
                        trabajaba en la industria textil. Conociendo que esta cooperativa estaba en marcha, y teniendo
                        familiares y amigos en la ciudad, tomo la decisión de volverse a Paraná, asociándose y aportando
                        un nuevo impulso, trayendo su experiencia y energía de la forma de trabajar en las fábricas más
                        grandes de Córdoba.<br>

                        En la última Asamblea Ordinaria, llevada a cabo con una exitosa participación de prácticamente
                        la totalidad de nuestros asociados, se renovó completamente el Consejo de Administración, siendo
                        elegido Presidente Diego Gomez, quien previamente venia cumpliendo funciones en la sección
                        comercial y de relaciones institucionales.
                    </p>
